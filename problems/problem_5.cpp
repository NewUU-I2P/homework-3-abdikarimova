float problemSolution5(float x, float y, char operation) {
   float result;
   if (operation = '+') {
      cout<<x+y;
   } else if(operation = '-'){
      cout << x - y;
   } else if (operation = '*') {
      cout << x*y;
   } else if (operation = '/') {
      cout << x/y;
   }


   return result;
}
